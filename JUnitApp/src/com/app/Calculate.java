package com.app;

public class Calculate {

	public int calculateAdd(int a,int b)
	{
		return a+b;
	}
	public int calculateSubtract(int a,int b)
	
	{
		return a-b;
	}
	
	public int calculateMultiply(int a,int b)
	{
		return a*b;
	}
	
	public int calculateDivide(int a,int b)
	{
		return a/b;
	}
	


	
}
